package net.confex.application;

import org.eclipse.ui.application.IWorkbenchConfigurer;
import org.eclipse.ui.application.IWorkbenchWindowConfigurer;
import org.eclipse.ui.application.WorkbenchAdvisor;
import org.eclipse.ui.application.WorkbenchWindowAdvisor;
import org.eclipse.ui.console.ConsolePlugin;
import org.eclipse.ui.console.IConsole;


/**
 * This workbench advisor creates the window advisor, and specifies
 * the perspective id for the initial window.
 */
public class ApplicationWorkbenchAdvisor extends WorkbenchAdvisor {
	
	private static final String PERSPECTIVE_ID = "net.confex.application.perspective";

    public WorkbenchWindowAdvisor createWorkbenchWindowAdvisor(IWorkbenchWindowConfigurer configurer) {
        return new ApplicationWorkbenchWindowAdvisor(configurer);
    }

	public String getInitialWindowPerspectiveId() {
		return PERSPECTIVE_ID;
	} 
	
	public void initialize(IWorkbenchConfigurer configurer) {
		System.out.println("ApplicationWorkbenchAdvisor initialize.");
		//configurer.setSaveAndRestore(ConfexPlugin.getDefault().isEnableEditMode());
		if(ConfexPlugin.getDefault().isEnableEditMode()) {
			configurer.setSaveAndRestore(true);
		} else {
			configurer.setSaveAndRestore(false);
		}
	}
	
}
